/**
 * Digital ID / QR — продуктовые правила и контракт с CRM (реализация на сервере позже).
 *
 * При создании игрока в CRM:
 * - генерируется уникальный постоянный токен (Digital ID / QR payload);
 * - токен сохраняется в БД и навсегда закрепляется за записью игрока;
 * - у двух игроков не может быть одного и того же токена (уникальность на уровне БД);
 * - токен не переносится между игроками и не переназначается;
 * - по токену формируется публичная (или защищённая) ссылка на страницу карточки игрока;
 * - при сканировании QR камерой пользователь переходит по этой ссылке;
 * - опционально: отдельный URL готового изображения QR (`qrCodeImageUrl`) или генерация на лету.
 *
 * Ожидаемые поля API (имена ориентировочные, согласовать с CRM):
 * - `digitalIdToken` — уникальный строковый идентификатор;
 * - `digitalIdUrl` — URL страницы карточки (для отображения/копирования);
 * - `qrCodeImageUrl` — опционально, PNG/SVG QR для отображения в приложении.
 */

